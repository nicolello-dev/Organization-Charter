import Head from "next/head";
import { useEffect, useState } from "react";

import type { EmployeeData } from "@/components/types";

import Filter from "@/components/filter"
import ViewData from "@/components/viewData";

export default function Home() {

  const [data, setData] = useState<EmployeeData | undefined>(undefined)

  useEffect(() => {
    fetch('/api/getSheet')
      .then(r => r.json())
      .then((r: EmployeeData) => setData(r))
      .catch(err => console.error(err));
  }, []);

  return (
    <>
      <Head>
        <title>Organization Charter - Improved</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="flex min-h-screen flex-col items-center justify-center bg-white dark:bg-black">
        <Filter/>
        <ViewData data={data}/>
      </main>
    </>
  );
}